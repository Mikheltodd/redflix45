    private void jButtonGuardarSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarSActionPerformed
        // TODO add your handling code here:
        String idSerie = jTextFieldTituloSerie.getText();
        Optional<Shows> opt = showRepository.findById(jTextFieldTituloSerie.getText());
        if (!idSerie.isEmpty()) {
            try {
                if (opt.isPresent()) {
                    showRepository.deleteById(idSerie);
                    JOptionPane.showMessageDialog(null, "Se ha eliminado correctamente la serie " + opt.get().getShowTitle(), "", JOptionPane.WARNING_MESSAGE);

                } else {
                    JOptionPane.showMessageDialog(null, "No se ha encontrado la serie con nombre " + idSerie, "", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {
                System.out.println("Error al eliminar la serie");
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error al eliminar la serie", "Warning", JOptionPane.WARNING_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "No ha digitado un titulo de serie", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_jButtonGuardarSActionPerformed

